package com.koscom.apply.model;

public class ApplyCzReturn {
	
	private String mode;
	private String id_coop;
	private String no_coop;
	private String status;
	private String memo;
	private String amt_approval;
	private String ymd_approval;
	
	
	public String getMode() {
		return mode;
	}
	public void setMode(String mode) {
		this.mode = mode;
	}
	public String getId_coop() {
		return id_coop;
	}
	public void setId_coop(String id_coop) {
		this.id_coop = id_coop;
	}
	public String getNo_coop() {
		return no_coop;
	}
	public void setNo_coop(String no_coop) {
		this.no_coop = no_coop;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
	public String getAmt_approval() {
		return amt_approval;
	}
	public void setAmt_approval(String amt_approval) {
		this.amt_approval = amt_approval;
	}
	public String getYmd_approval() {
		return ymd_approval;
	}
	public void setYmd_approval(String ymd_approval) {
		this.ymd_approval = ymd_approval;
	}
}
